void main() {
  int a = 10;
  int b = 3;

  // Operator Aritmatika
  print("=== Operator Aritmatika ===");
  print("$a + $b = ${a + b}");
  print("$a - $b = ${a - b}");
  print("$a * $b = ${a * b}");
  print("$a / $b = ${a / b}");
  print("$a % $b = ${a % b}");

  // Operator Relasional
  print("\n=== Operator Relasional ===");
  print("$a > $b = ${a > b}");
  print("$a < $b = ${a < b}");
  print("$a == $b = ${a == b}");
  print("$a != $b = ${a != b}");

  // Operator Logika
  bool x = true;
  bool y = false;
  print("\n=== Operator Logika ===");
  print("x && y = ${x && y}");
  print("x || y = ${x || y}");
  print("!x = ${!x}");
}
